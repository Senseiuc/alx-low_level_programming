/**
 * op_add - add two integers
 *@a: The first integer
 *@b: The second integer
 *
 *Return: integer
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two integers
 *@a: The first integer
 *@b: The second integer
 *
 *Return: integer
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiply two integers
 *@a: The first integer
 *@b: The second integer
 *
 *Return: integer
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divide an integer with another
 *@a: The first integer
 *@b: The divisor
 *
 *Return: integer
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - find the mod B of int A
 *@a: The first integer
 *@b: The second integer
 *
 *Return: integer
 */
int op_mod(int a, int b)
{
	return (a % b);
}
